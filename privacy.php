                        </li>
                        <li>File integrity is verified using HMAC-SHA256 authentication.</li>
                        <li>Encryption keys are securely stored and never exposed in code or public areas.</li>
                    </ul>

                    <h3>4.2 Access Controls</h3>
                    <ul>
                        <li>Role-based access control (RBAC) limits data access to authorized personnel.</li>
                        <li>Multi-factor authentication options for enhanced account security.</li>
                        <li>
                            User documents are accessible only to the account owner and authorized administrators.
                        </li>
                        <li>All access to sensitive data is logged and monitored.</li>
                    </ul>

                    <h3>4.3 Secure Transmission</h3>
                    <ul>
                        <li>
                            All data transmitted between your browser and our servers is encrypted using SSL/TLS
                            protocols.
                        </li>
                        <li>HTTPS is enforced across our entire platform.</li>
                    </ul>

                    <h3>4.4 Physical Security</h3>
                    <ul>
                        <li>Servers are housed in secure, access-controlled facilities.</li>
                        <li>Regular security audits and vulnerability assessments.</li>
                        <li>Backup systems with encryption for disaster recovery.</li>
                    </ul>

                    <h3>4.5 Secure File Handling</h3>
                    <ul>
                        <li>Original unencrypted files are permanently deleted after encryption.</li>
                        <li>
                            Decrypted files are only created temporarily when you view documents and are
                            immediately deleted after serving.
                        </li>
                        <li>No permanent copies of decrypted files are stored on our servers.</li>
                    </ul>
