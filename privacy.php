                    <h2>4. Data Security and Encryption</h2>
                    <p>We implement industry-standard security measures to protect your information:</p>

                    <h3>4.1 Encryption</h3>
                    <ul>
                        <li>All uploaded documents are encrypted using AES-256-CBC encryption before storage.</li>
                        <li>Encrypted files are stored with .enc extension and cannot be accessed without decryption keys.</li>
                        <li>
